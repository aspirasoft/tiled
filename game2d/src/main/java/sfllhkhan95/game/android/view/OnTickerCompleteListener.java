package sfllhkhan95.game.android.view;

/**
 * Interface definition for the callback to be invoked when TickerTextView has been displayed
 * completely.
 *
 * @author saifkhichi96
 * @version 1.0
 */
public interface OnTickerCompleteListener {

    /**
     * This method is invoked when TickerTextView has completed its animation.
     */
    void onTickerComplete();

}
